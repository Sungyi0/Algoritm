#include <iostream>
#include <vector>
#include <string>

using namespace std;

/*
	문제 출처: try-cat.ch
	URL : http://www.try-cat.ch/contest/view/exercise/30/%EC%A4%91%EB%B3%B5%EB%90%98%EB%8A%94-%EC%88%98-%EC%A0%9C%EA%B1%B0

	문제:
	정수의 배열 중 중복되는 수를 제거하라
	임의의 정수의 배열을 입력하였을 때 배열요소 중 중복되는 요소를 제거한 뒤 출력하라.

	제한:
	Time Limit : 200ms, Memory Limit : 512kb

	임의의 정수 배열이 입력되고, 각 정수는 쉼표(,)로 구분된다.
	입력:
	1,3,2,4,2,4,4
	
	중복된 수가 제거된 정수 배열을 별다른 정렬 없이 입력된 순서대로 출력한
	출력:
	1,3,2,4
*/

int main()
{
	string input;
	getline(cin, input);

	vector<int> arr(1);
	// string으로 입력받고 쉼표를 구분자로 parsing합니다.
	{
		int cursor = 0;
		for (string::const_iterator iter = input.begin(); iter != input.end(); ++iter)
		{
			if ((*iter) == ',')
			{
				// 중복되는 수는 무시합니다.
				if (find(arr.begin(), arr.end() -1, arr[cursor]) != arr.end() -1)
				{
					arr[cursor] = 0;
				}
				else
				{
					++cursor;
					arr.push_back(0);
				}
				continue;
			}

			// intInput[iter] (십진수)<< 1;
			arr[cursor] *= 10;
			arr[cursor] += (*iter) - '0';
		}
	}

	for (vector<int>::const_iterator iter = arr.begin(); iter != arr.end(); ++iter)
		cout << (*iter) << ',';

	return 0;
}